def build_sector_concept_maps() -> tuple[dict[str, str], dict[str, str]]:
    """Build stock_code -> sector and stock_code -> concepts using multi-source APIs.

    Strategy (tries in order):
    1. 东方财富 (em): board constituent API - most complete when available
    2. 同花顺 (ths): board names API - usually works, but no constituent function
    3. 新浪 (sina): stock classification - slow but reliable, from vip.stock.finance.sina.com.cn
    4. Fallback: 东方财富 spot API for basic sector data only
    Returns (sector_map, concept_map).
    """
    sector_map: dict[str, str] = {}
    concept_map: dict[str, str] = {}

    # --- Phase 1: Get board names from 同花顺 (usually works) ---
    ths_conc_names = []
    ths_ind_names = []
    try:
        df = ak.stock_board_concept_name_ths()
        ths_conc_names = df["name"].tolist()
    except Exception:
        pass
    try:
        df = ak.stock_board_industry_name_ths()
        ths_ind_names = df["name"].tolist()
    except Exception:
        pass

    # --- Phase 2: Try 东方财富 (em) for constituent stocks ---
    em_available = False
    try:
        for i, bname in enumerate(ths_ind_names):
            try:
                cons_df = ak.stock_board_industry_cons_em(symbol=bname)
                for _, row in cons_df.iterrows():
                    raw_code = str(row.get("代码", "")).strip().lower()
                    for p in ("sh", "sz", "bj"):
                        if raw_code.startswith(p): raw_code = raw_code[2:]
                    code = raw_code.zfill(6)
                    if bname not in sector_map.get(code, ""):
                        sector_map[code] = bname
            except Exception:
                pass
            if (i + 1) % 30 == 0 and i > 0:
                import time as _t; _t.sleep(0.3)
        em_available = bool(sector_map)
    except Exception:
        pass

    if em_available:
        # Try concept boards via em
        try:
            for i, bname in enumerate(ths_conc_names):
                try:
                    cons_df = ak.stock_board_concept_cons_em(symbol=bname)
                    for _, row in cons_df.iterrows():
                        raw_code = str(row.get("代码", "")).strip().lower()
                        for p in ("sh", "sz", "bj"): raw_code = raw_code[2:]
                        code = raw_code.zfill(6)
                        if code not in concept_map:
                            concept_map[code] = bname
                        else:
                            concept_map[code] += "," + bname
                except Exception:
                    pass
                if (i + 1) % 30 == 0 and i > 0:
                    import time as _t; _t.sleep(0.3)
        except Exception:
            pass

    else:
        # --- Phase 3: Fallback via 新浪 (sina) stock classification ---
        print("[backfill] 东方财富 unavailable, trying 新浪...")
        try:
            # Get industry classification from Sina
            sind = ak.stock_classify_sina(symbol="申万行业")
            for _, row in sind.iterrows():
                code = str(row.get("symbol", "")).strip()
                industry = str(row.get("class", "")).strip()
                if code and industry and code not in sector_map:
                    sector_map[code] = industry

            # Get concept classification from Sina
            sconc = ak.stock_classify_sina(symbol="热门概念")
            for _, row in sconc.iterrows():
                code = str(row.get("symbol", "")).strip()
                concept = str(row.get("class", "")).strip()
                if code and concept:
                    if code not in concept_map:
                        concept_map[code] = concept
                    else:
                        concept_map[code] += "," + concept
            print(f"[backfill] 新浪: {len(sector_map)} sectors, {len(concept_map)} concepts")
        except Exception as e:
            print(f"[backfill] 新浪 also failed: {e}")

    # --- Phase 4: Final fallback via em spot API for basic sector data ---
    if not sector_map and not concept_map:
        try:
            df = ak.stock_zh_a_spot_em()
            if df is not None and not df.empty:
                col = next((c for c in ["行业", "所属行业"] if c in df.columns), None)
                if col is None:
                    for ci in range(25, min(32, df.shape[1])):
                        sample = df.iloc[:5, ci].dropna()
                        vals = [v for v in sample if str(v).strip() not in ("", "-", "nan", "None")]
                        if vals: col = df.columns[ci]; break
                if col:
                    for _, row in df.iterrows():
                        raw_code = str(row.get("代码", "")).strip().lower()
                        for p in ("sh", "sz", "bj"):
                            if raw_code.startswith(p): raw_code = raw_code[2:]
                        code = raw_code.zfill(6)
                        val = str(row.get(col, "")).strip()
                        if val and val not in ("", "-", "nan", "None"):
                            sector_map[code] = val
        except Exception:
            pass

    print(f"[backfill] Result: {len(sector_map)} sectors, {len(concept_map)} concepts")
    return sector_map, concept_map

