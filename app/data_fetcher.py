class MarketDataError(RuntimeError):
    pass


def filter_main_board_stocks(frame: pd.DataFrame) -> list[dict]:
    """Return active, non-risk Shanghai/Shenzhen main-board A shares."""
    required = {"代码", "名称", "最新价"}
    if frame is None or frame.empty or not required.issubset(frame.columns):
        raise MarketDataError("股票列表数据为空或字段不完整")

    stocks: list[dict] = []
    for _, row in frame.iterrows():
        raw_code = str(row["代码"]).strip().lower()
        if raw_code.startswith(("sh", "sz", "bj")):
            raw_code = raw_code[2:]
        code = raw_code.zfill(6)
        name = str(row["名称"]).strip()
        price = pd.to_numeric(row["最新价"], errors="coerce")
        upper_name = name.upper()
        if not code.startswith(MAIN_BOARD_PREFIXES):
            continue
        if "ST" in upper_name or "退市" in name:
            continue
        if pd.isna(price) or float(price) <= 0:
            continue
        stocks.append({
            "code": code,
            "name": name,
            "market": "sh" if code.startswith("6") else "sz",
            "latest_price": float(price),
        })
    return stocks


def fetch_stock_universe() -> list[dict]:
    errors = []
    for attempt in range(settings.max_retries):
        try:
            return filter_main_board_stocks(ak.stock_zh_a_spot_em())
        except Exception as exc:
            errors.append(f"东方财富: {exc}")
            if attempt + 1 < settings.max_retries:
                time.sleep(1.5 * (attempt + 1))
    try:
        return filter_main_board_stocks(ak.stock_zh_a_spot())
    except Exception as exc:
        errors.append(f"新浪: {exc}")
    raise MarketDataError("获取全市场股票列表失败；" + "；".join(errors))



