def _generate_synthetic_kline(code: str, days: int = 60):
    """Generate synthetic daily K-line data for demo/testing."""
    import numpy as np, datetime, pandas as pd
    np.random.seed(int(code) % 10000)
    base_price = np.random.uniform(5, 200)
    vol = base_price * 0.025
    records = []
    base_date = datetime.date.today()
    for i in range(days):
        td = base_date - datetime.timedelta(days=days - i)
        if td.weekday() >= 5: continue
        open_p = base_price + np.random.normal(0, vol)
        close_p = open_p + np.random.normal(0, vol * 0.5)
        high_p = max(open_p, close_p) + abs(np.random.normal(0, vol * 0.3))
        low_p = min(open_p, close_p) - abs(np.random.normal(0, vol * 0.3))
        v = np.random.uniform(100000, 5000000)
        records.append(dict(trade_date=td.strftime("%Y-%m-%d"), open=round(open_p,2),
            close=round(close_p,2), high=round(high_p,2), low=round(low_p,2),
            volume=v, amount=round(close_p*v/10000,2)))
    return pd.DataFrame(records)

def fetch_daily_kline(code: str, start_date: str, end_date: str) -> pd.DataFrame:
    """Fetch real adjusted history. Failure is explicit; no synthetic fallback."""
    market_code = ("sh" if code.startswith("6") else "sz") + code
    errors = []

    try:
        frame = ak.stock_zh_a_hist_tx(
            symbol=market_code,
            start_date=start_date,
            end_date=end_date,
            adjust="qfq",
            timeout=20,
        )
        if frame is not None and not frame.empty:
            result = frame.rename(columns={
                "date": "trade_date", "amount": "volume",
            })[["trade_date", "open", "close", "high", "low", "volume"]].copy()
            result["amount"] = (
                pd.to_numeric(result["close"], errors="coerce")
                * pd.to_numeric(result["volume"], errors="coerce") * 100
            )
            return _normalize_history(code, result)
        errors.append("腾讯: 无数据")
    except Exception as exc:
        errors.append(f"腾讯: {exc}")

    try:
        frame = ak.stock_zh_a_hist(
            symbol=code, period="daily", start_date=start_date,
            end_date=end_date, adjust="qfq", timeout=20,
        )
        if frame is not None and not frame.empty:
            result = frame.rename(columns={
                "日期": "trade_date", "开盘": "open", "收盘": "close",
                "最高": "high", "最低": "low", "成交量": "volume",
                "成交额": "amount",
            })[
                ["trade_date", "open", "close", "high", "low", "volume", "amount"]
            ].copy()
            return _normalize_history(code, result)
        errors.append("东方财富: 无数据")
    except Exception as exc:
        errors.append(f"东方财富: {exc}")

    try:
        frame = ak.stock_zh_a_daily(
            symbol=market_code,
            start_date=start_date,
            end_date=end_date,
            adjust="qfq",
        )
        if frame is not None and not frame.empty:
            result = frame.rename(columns={"date": "trade_date"})[
                ["trade_date", "open", "close", "high", "low", "volume", "amount"]
            ].copy()
            return _normalize_history(code, result)
        errors.append("新浪: 无数据")
    except Exception as exc:
        errors.append(f"新浪: {exc}")

    raise MarketDataError(f"{code} 日线获取失败；" + "；".join(errors))


def _normalize_history(code: str, result: pd.DataFrame) -> pd.DataFrame:
    result["trade_date"] = pd.to_datetime(result["trade_date"]).dt.strftime("%Y-%m-%d")
    for column in ("open", "close", "high", "low", "volume", "amount"):
        result[column] = pd.to_numeric(result[column], errors="coerce")
    result = result.dropna(subset=["open", "close", "high", "low", "volume"])
    if result.empty:
        raise MarketDataError(f"{code} 日线数据无有效记录")
    return result.sort_values("trade_date").reset_index(drop=True)


def latest_expected_trade_date(today: datetime.date | None = None) -> datetime.date:
    """Return the latest expected session date, with a network-free fallback."""
    current = today or datetime.date.today()
    try:
        calendar = ak.tool_trade_date_hist_sina()
        dates = pd.to_datetime(calendar["trade_date"]).dt.date
        eligible = dates[dates <= current]
        if not eligible.empty:
            return max(eligible)
    except Exception:
        pass
    while current.weekday() >= 5:
        current -= datetime.timedelta(days=1)
    return current


