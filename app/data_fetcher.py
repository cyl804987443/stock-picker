async def sync_market_data(
    stocks: list[dict],
    progress_callback: ProgressCallback | None = None,
) -> dict:
    if not stocks:
        raise MarketDataError("股票池为空")
    latest_dates = await _latest_cache_dates()
    target_date = await asyncio.to_thread(latest_expected_trade_date)
    initial_start = target_date - datetime.timedelta(days=190)
    semaphore = asyncio.Semaphore(settings.market_data_concurrency)
    total = len(stocks)
    processed = success = failed = 0
    errors: list[str] = []

    async def fetch_one(item: dict):
        code = item["code"]
        latest = latest_dates.get(code)
        start = (
            datetime.date.fromisoformat(latest) + datetime.timedelta(days=1)
            if latest else initial_start
        )
        if start > target_date:
            return code, None, None
        async with semaphore:
            try:
                frame = await asyncio.to_thread(
                    fetch_daily_kline,
                    code,
                    start.strftime("%Y%m%d"),
                    target_date.strftime("%Y%m%d"),
                )
                if latest is None:
                    frame = frame.tail(settings.initial_history_days)
                return code, frame, None
            except MarketDataError as exc:
                return code, None, str(exc)

    tasks = [asyncio.create_task(fetch_one(item)) for item in stocks]
    for future in asyncio.as_completed(tasks):
        code, frame, error = await future
        processed += 1
        if error:
            failed += 1
            errors.append(error)
        else:
            if frame is not None and not frame.empty:
                await _store_history(code, frame)
            success += 1
        if progress_callback:
            await progress_callback(processed, total, success, failed)

    ratio = success / total
    if ratio < settings.minimum_sync_ratio:
        raise MarketDataError(
            f"真实行情同步覆盖率仅 {ratio:.1%}，低于要求的 "
            f"{settings.minimum_sync_ratio:.0%}；未执行选股。"
        )
    return {
        "total": total, "processed": processed, "success": success,
        "failed": failed, "errors": errors[:20],
    }


async def get_cached_data(
    codes: list[str] | None = None,
    start_date: str | None = None,
    end_date: str | None = None,
) -> dict[str, pd.DataFrame]:
    query = select(StockCache)
    if codes:
        query = query.where(StockCache.stock_code.in_(codes))
    if start_date:
        query = query.where(StockCache.trade_date >= start_date)
    if end_date:
        query = query.where(StockCache.trade_date <= end_date)
    query = query.order_by(StockCache.stock_code, StockCache.trade_date)
    async with async_session() as session:
        rows = (await session.execute(query)).scalars().all()

    grouped: dict[str, list[dict]] = {}
    for row in rows:
        grouped.setdefault(row.stock_code, []).append({
            "trade_date": row.trade_date, "open": row.open or 0,
            "close": row.close or 0, "high": row.high or 0,
            "low": row.low or 0, "volume": row.volume or 0,
            "amount": row.amount or 0,
        })
    return {
        code: pd.DataFrame(records).sort_values("trade_date").reset_index(drop=True)
        for code, records in grouped.items()
    }
